export function suma(a, b) {

  return a + b;

}



export function resta(a, b) {

  return a - b;

}



export function multiplicacion(a, b) {

  return a * b;

}



export function division(a, b) {

  return a / b;

}



export function potencia(a, b) {

  return Math.pow(a, b);

}



export function raizCuadrada(a) {

  return Math.sqrt(a);

}

export default { suma, resta, multiplicacion, division, potencia, raizCuadrada };